/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode *getIntersectionNode(ListNode *headA, ListNode *headB) {
        vector<ListNode *> A;
        vector<ListNode *> B;
        ListNode* res=0;
        while(headA->next!=NULL) A.push_back(headA->next);
        while(headB->next!=NULL) B.push_back(headB->next);
        int i=A.size()-1;
        int j=B.size()-1;
        while(A[i]==B[j]){
            res=A[i];
            i--;
            j--;
        }
        return res;
    }
};